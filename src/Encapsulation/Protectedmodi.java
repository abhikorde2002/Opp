package Encapsulation;
//Info extends Protected modifier class.
//In this modifier we can access variable in same class ,package or outside in the child class of this class.
public class Protectedmodi {
    protected int Ak;
    protected String name ;

    public Protectedmodi(int ak, String name) {
        Ak = ak;
        this.name = name;
    }

    public Protectedmodi() {

    }
}
